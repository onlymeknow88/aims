use Illuminate\Validation\ValidationException;
use Livewire\Component;

class Login extends Component
{
    public $email;
    public $password;
    public $ingat;

    use ThrottlesLogins;


    public function render()
    {
        return view('livewire.auth.login')->extends('layouts.no-header');
    }

    protected $rules = [
        'email' => 'required',
        'password' => 'required',
    ];

    public function loginStore(){
